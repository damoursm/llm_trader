def send_recommendations(recommendations: List[Recommendation], total_analysed: int = 0, performance: dict = None) -> bool:
    """Render and send the recommendation email."""
    if not recommendations:
        logger.warning("No recommendations to send.")
        return False

    now_str = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    stocks = sorted([r for r in recommendations if r.type == "STOCK"], key=lambda x: x.confidence, reverse=True)
    etfs = sorted([r for r in recommendations if r.type == "ETF"], key=lambda x: x.confidence, reverse=True)

    html_body = Template(HTML_TEMPLATE).render(
        stocks=stocks,
        etfs=etfs,
        generated_at=now_str,
        total=total_analysed or len(recommendations),
        colors=ACTION_COLOR,
        perf=performance,
    )

    # Plain text fallback
    lines = [f"Actionable Signals — {now_str}", ""]
    if stocks:
        lines.append("STOCKS")
        for rec in stocks:
            lines.append(f"  {rec.ticker}: {rec.action} ({rec.direction}, {rec.confidence:.0%})")
            lines.append(f"    {rec.rationale}")
            lines.append("")
    if etfs:
        lines.append("ETFs / MARKETS")
        for rec in etfs:
            lines.append(f"  {rec.ticker}: {rec.action} ({rec.direction}, {rec.confidence:.0%})")
            lines.append(f"    {rec.rationale}")
            lines.append("")
    text_body = "\n".join(lines)

    buys = [r.ticker for r in recommendations if r.action == "BUY"]
    subject = f"LLM Trader | {now_str} | BUY: {', '.join(buys) if buys else 'none'}"

    try:
        msg = MIMEMultipart("alternative")
        msg["Subject"] = subject
        msg["From"] = settings.smtp_user
        msg["To"] = ", ".join(settings.recipients_list)
        msg.attach(MIMEText(text_body, "plain"))
        msg.attach(MIMEText(html_body, "html"))

        with smtplib.SMTP(settings.smtp_host, settings.smtp_port) as server:
            server.starttls()
            server.login(settings.smtp_user, settings.smtp_password)
            server.sendmail(
                settings.smtp_user,
                settings.recipients_list,
                msg.as_string(),
            )

        logger.info(f"Email sent to {settings.recipients_list}")
        return True
    except Exception as e:
        logger.error(f"Failed to send email: {e}")
        return False
